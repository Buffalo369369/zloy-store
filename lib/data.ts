export type BrandSlug = "hilma" | "driada" | "somatrop" | "marten";

export function isBrandSlug(x: string): x is BrandSlug {
  return x === "hilma" || x === "driada" || x === "somatrop" || x === "marten";
}

export function getProductsByBrand(slug: BrandSlug) {
  return products.filter((p) => p.category === slug);
}
export function interleaveByBrand(items: Product[]) {
  const order: BrandSlug[] = ["hilma", "driada", "somatrop", "marten"];

  // 
  const buckets: Record<BrandSlug, Product[]> = {
    hilma: [],
    driada: [],
    somatrop: [],
    marten: [],
  };

  for (const p of items) {
    // 
    if (isBrandSlug(p.category)) buckets[p.category].push(p);
  }

  const result: Product[] = [];
  let added = true;

  while (added) {
    added = false;

    for (const b of order) {
      if (buckets[b].length) {
        result.push(buckets[b].shift()!);
        added = true;
      }
    }
  }

  return result;
}